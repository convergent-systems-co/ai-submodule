#!/bin/bash
# governance/bin/generate-project-yaml.sh — Generate project.yaml from detected language.
#
# Accepts language detection JSON (from detect-language.sh) and produces a
# project.yaml using the matching template from governance/templates/{language}/.
# Substitutes the repo name and sets smart defaults.
#
# Usage:
#   bash .ai/governance/bin/generate-project-yaml.sh [OPTIONS]
#
# Options:
#   --language <lang>    Language to use (e.g., python, node, go)
#   --json <json>        Full detection JSON from detect-language.sh
#   --output <path>      Write to file instead of stdout
#   --repo-name <name>   Override repo name (default: basename of repo root)
#   --repo-root <path>   Override repo root (default: parent of AI_DIR or git toplevel)
#
# Examples:
#   bash .ai/governance/bin/generate-project-yaml.sh --language python
#   bash .ai/governance/bin/generate-project-yaml.sh --json '{"language":"node","framework":"react"}'
#   bash .ai/governance/bin/generate-project-yaml.sh --language go --output project.yaml

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
AI_DIR="${AI_DIR:-$(cd "$SCRIPT_DIR/../.." && pwd)}"

# Source common library if available
if [ -f "$SCRIPT_DIR/lib/common.sh" ]; then
  source "$SCRIPT_DIR/lib/common.sh"
fi

# --- Parse arguments ---
LANGUAGE=""
DETECTION_JSON=""
OUTPUT_PATH=""
REPO_NAME=""
REPO_ROOT="${REPO_ROOT:-}"

while [[ $# -gt 0 ]]; do
  case "$1" in
    --language) LANGUAGE="$2"; shift 2 ;;
    --json) DETECTION_JSON="$2"; shift 2 ;;
    --output) OUTPUT_PATH="$2"; shift 2 ;;
    --repo-name) REPO_NAME="$2"; shift 2 ;;
    --repo-root) REPO_ROOT="$2"; shift 2 ;;
    *)
      echo "Unknown argument: $1" >&2
      echo "Usage: $(basename "$0") --language <lang> [--output <path>] [--repo-name <name>]" >&2
      exit 1
      ;;
  esac
done

# --- Resolve language from JSON if provided ---
if [ -n "$DETECTION_JSON" ] && [ -z "$LANGUAGE" ]; then
  # Extract language from JSON without requiring jq
  LANGUAGE=$(echo "$DETECTION_JSON" | sed -n 's/.*"language" *: *"\([^"]*\)".*/\1/p')
  if [ -z "$LANGUAGE" ]; then
    # Try compact JSON format
    LANGUAGE=$(echo "$DETECTION_JSON" | sed -n 's/.*"language":"\([^"]*\)".*/\1/p')
  fi
fi

if [ -z "$LANGUAGE" ]; then
  echo "Error: --language or --json with a detected language is required" >&2
  exit 1
fi

# --- Resolve repo root and name ---
if [ -z "$REPO_ROOT" ]; then
  if [ -n "${AI_DIR:-}" ]; then
    REPO_ROOT="$(dirname "$AI_DIR")"
  elif git rev-parse --is-inside-work-tree &>/dev/null; then
    REPO_ROOT="$(git rev-parse --show-toplevel)"
  else
    REPO_ROOT="$(pwd)"
  fi
fi

if [ -z "$REPO_NAME" ]; then
  REPO_NAME="$(basename "$REPO_ROOT")"
fi

# --- Find template ---
TEMPLATE_DIR="$AI_DIR/governance/templates"
TEMPLATE_FILE=""

# Check direct language match
if [ -f "$TEMPLATE_DIR/$LANGUAGE/project.yaml" ]; then
  TEMPLATE_FILE="$TEMPLATE_DIR/$LANGUAGE/project.yaml"
# Check languages subdirectory (alternate layout)
elif [ -f "$TEMPLATE_DIR/languages/$LANGUAGE/project.yaml" ]; then
  TEMPLATE_FILE="$TEMPLATE_DIR/languages/$LANGUAGE/project.yaml"
# Fallback to generic template
elif [ -f "$TEMPLATE_DIR/project.yaml" ]; then
  TEMPLATE_FILE="$TEMPLATE_DIR/project.yaml"
else
  echo "Error: No template found for language '$LANGUAGE'" >&2
  echo "Searched: $TEMPLATE_DIR/$LANGUAGE/project.yaml" >&2
  echo "Searched: $TEMPLATE_DIR/languages/$LANGUAGE/project.yaml" >&2
  echo "Searched: $TEMPLATE_DIR/project.yaml" >&2
  exit 1
fi

# --- Extract framework from detection JSON ---
FRAMEWORK=""
if [ -n "$DETECTION_JSON" ]; then
  FRAMEWORK=$(echo "$DETECTION_JSON" | sed -n 's/.*"framework" *: *"\([^"]*\)".*/\1/p')
  if [ -z "$FRAMEWORK" ]; then
    FRAMEWORK=$(echo "$DETECTION_JSON" | sed -n 's/.*"framework":"\([^"]*\)".*/\1/p')
  fi
fi

# --- Generate project.yaml from template ---
# Read template and substitute values
generate_yaml() {
  local template="$1"
  local repo_name="$2"
  local framework="$3"

  # Read the template
  local content
  content="$(cat "$template")"

  # Replace the project name (first 'name:' field)
  content="$(echo "$content" | sed "s/^name: .*/name: \"$repo_name\"/")"

  # Replace framework if detected and non-empty
  if [ -n "$framework" ]; then
    content="$(echo "$content" | sed "s/^framework: .*/framework: \"$framework\"/")"
  fi

  # Remove the "Copy to project.yaml" comment lines (not needed in generated output)
  content="$(echo "$content" | sed '/^# Copy to project.yaml/d')"

  # Add generation header
  cat <<HEADER
# Project AI Configuration — $repo_name
# Auto-generated by detect-language + generate-project-yaml
# Language detected: $LANGUAGE
# Template: $(basename "$(dirname "$template")")/project.yaml
#
# Customize this file for your project. Only 'name' and 'language'/'languages' are required.
# All other fields have sensible defaults provided by the governance schema.

HEADER

  # Add governance section with smart defaults if not present in template
  if ! echo "$content" | grep -q "^governance:"; then
    # Insert governance defaults after the panels section
    echo "$content" | sed '/^# repository:/i\
governance:\
  policy_profile: "default"\
  parallel_coders: 5\
'
  else
    echo "$content"
  fi
}

# --- Output ---
output="$(generate_yaml "$TEMPLATE_FILE" "$REPO_NAME" "$FRAMEWORK")"

if [ -n "$OUTPUT_PATH" ]; then
  # Ensure parent directory exists
  mkdir -p "$(dirname "$OUTPUT_PATH")"
  echo "$output" > "$OUTPUT_PATH"
  echo "Generated project.yaml at: $OUTPUT_PATH" >&2
else
  echo "$output"
fi
